package Day5;

import java.util.ArrayList;

public class Task1 {

    public static void main(String[] args) {

        ArrayList<String> cities = new ArrayList<>();

        cities.add("Hatay");
        cities.add("Aydın");
        cities.add("İzmir");
        cities.add("Denizli");
        cities.add("Muğla");

        for (String city : cities) {
            System.out.println(city);
        }
    }

    /* 1. ArrayList sınıfını kullanarak bir ArrayList oluşturun ve
    içine 5 farklı şehir ekleyin. Bu şehirleri ekrana yazdırın.
*/
}

